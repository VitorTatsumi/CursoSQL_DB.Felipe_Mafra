--UMA ESTRUTURA IF ELSE SIMPLES

BEGIN
IF 
		'VITORIA BEATRIIZ' IN (
								SELECT 
									A1_NOME 
								FROM 
									SA1020 
								WHERE 
									A1_NOME LIKE '%VITOR%'
							)
		PRINT 'ENCONTRADO'
ELSE 
		PRINT 'NOTTODAYSATAN'
END

