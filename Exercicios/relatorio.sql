SELECT DISTINCT TOP 10 R.A3_NOME AS REPRESENTANTE, C5_NUM AS PEDIDO, C6_QTDVEN AS QUANTIDADE, C5_NOTA AS NF
FROM SA3020 AS R
	INNER JOIN SC5020 AS P
ON R.A3_COD = P.C5_VEND1 AND R.D_E_L_E_T_ = ' '
	INNER JOIN SC6020 AS Q
ON C5_NUM = C6_NUM AND 
   Q.D_E_L_E_T_ = ' '
WHERE Q.D_E_L_E_T_ = ' ' AND 
      C5_EMISSAO BETWEEN '20230800' AND '20230900';
